#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		ft_putnbr(n / 10);
		n %= 10;
		n *= (-1);
	}
	if (n < 0)
	{
		ft_putchar('-');
		n *= (-1);
	}
	if (n >= 0 && n <= 9)
		ft_putchar(n + '0');
	if (n / 10 == 0)
		return ;
	ft_putnbr(n / 10);
	ft_putchar(n % 10 + '0');
}
