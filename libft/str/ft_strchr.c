#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s != (char)c && *s != '\0')
		s++;
	if (*s == '\0' && (char)c == '\0')
		return ((char *)s);
	else if (*s == '\0' && (char)c != '\0')
		return (NULL);
	return ((char *)s);
}
